/**
 * 一次性：把 PostgreSQL 的資料匯出成 D1(SQLite) 可直接灌入的 SQL。
 *
 * 用法（在 repo 根目錄，這裡已有 pg 相依）：
 *   DATABASE_URL="postgresql://user:pass@host:5432/db" node scripts/migrate-pg-to-d1.mjs > cf/d1-import.sql
 *   cd cf && npx wrangler d1 execute ehr-104 --remote --file=d1-import.sql
 *
 * schema-aware：自動偵測來源實際欄位 ——
 *   - 來源沒有的「表」（例如舊版正式環境沒有 Delegation）→ 略過，D1 該表留空。
 *   - 來源沒有的「欄位」（例如舊版 UsageLog 沒有 actorUserId）→ 該欄填 NULL。
 * 轉換：日期→ISO 字串、boolean→0/1、保留主鍵 id、最後校正 autoincrement。
 *
 * 本腳本只「讀」Postgres（SELECT）並把 SQL 印到 stdout，不寫入任何資料庫、不碰 .env。
 */

import pg from 'pg';

const url = process.env.DATABASE_URL;
if (!url) {
  console.error('ERROR: 請設定 DATABASE_URL，例如：DATABASE_URL="postgresql://..." node scripts/migrate-pg-to-d1.mjs > cf/d1-import.sql');
  process.exit(1);
}

/** 把 JS 值轉成 SQLite SQL literal。 */
function lit(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return Number.isFinite(v) ? String(v) : 'NULL';
  if (typeof v === 'boolean') return v ? '1' : '0';
  if (v instanceof Date) return `'${v.toISOString()}'`;
  return `'${String(v).replace(/'/g, "''")}'`;
}

// 目標 D1 欄位（順序對應 D1 的 INSERT）
const TABLES = [
  { name: 'UserBinding', cols: ['id', 'lineUserId', 'companyId', 'internalCompanyId', 'empId', 'encryptedToken', 'iv', 'cookies', 'createdAt', 'updatedAt'] },
  { name: 'ScheduledTask', cols: ['id', 'userId', 'scheduledAt', 'lat', 'lng', 'status', 'result', 'createdAt', 'updatedAt'] },
  { name: 'UsageLog', cols: ['id', 'userId', 'actorUserId', 'action', 'count', 'details', 'createdAt'] },
  { name: 'Delegation', cols: ['id', 'granterId', 'granteeId', 'active', 'expiresAt', 'createdAt', 'updatedAt'] },
];

const client = new pg.Client({ connectionString: url });
await client.connect();

async function tableExists(name) {
  const r = await client.query('SELECT to_regclass($1) AS x', [`public."${name}"`]);
  return !!r.rows[0].x;
}
async function sourceCols(name) {
  const r = await client.query('SELECT column_name FROM information_schema.columns WHERE table_name=$1', [name]);
  return new Set(r.rows.map((x) => x.column_name));
}

// 不放 BEGIN/COMMIT/PRAGMA：D1 的 `wrangler d1 execute --file` 會自行原子處理整個檔，
// 在 SQL 內寫 BEGIN TRANSACTION 反而會被 D1 拒絕。
const out = [];
out.push('-- Generated by scripts/migrate-pg-to-d1.mjs');

const importedTables = [];
let totalRows = 0;
for (const t of TABLES) {
  if (!(await tableExists(t.name))) {
    console.error(`  ${t.name}: 來源不存在，略過`);
    continue;
  }
  const src = await sourceCols(t.name);
  const present = t.cols.filter((c) => src.has(c));
  const missing = t.cols.filter((c) => !src.has(c));
  const quoted = present.map((c) => `"${c}"`).join(', ');
  const { rows } = await client.query(`SELECT ${quoted} FROM "${t.name}" ORDER BY "id"`);
  console.error(`  ${t.name}: ${rows.length} rows（來源缺欄位填 NULL：${missing.join(', ') || '無'}）`);
  totalRows += rows.length;
  importedTables.push(t.name);
  for (const r of rows) {
    const vals = t.cols.map((c) => (src.has(c) ? lit(r[c]) : 'NULL')).join(', ');
    out.push(`INSERT INTO ${t.name} (${t.cols.join(', ')}) VALUES (${vals});`);
  }
}

out.push('-- 校正 autoincrement 計數器，避免新 id 與既有 id 相撞');
for (const name of importedTables) {
  out.push(`UPDATE sqlite_sequence SET seq = (SELECT COALESCE(MAX(id),0) FROM ${name}) WHERE name = '${name}';`);
}

await client.end();

process.stdout.write(out.join('\n') + '\n');
console.error(`\nDone. ${totalRows} rows total。重新導向到檔案後用 wrangler d1 execute 灌入。`);
